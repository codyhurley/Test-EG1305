
public class Pet {
	
	String name;
	
	String type;
	
	int age;
	
	public Pet() {
		
		
	}
	
	
	public Pet(String n, String t, int a) {
		
		name = n;
		type = t;
		age = a;
		
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getType() {
		return type;
	}


	public void setType(String type) {
		this.type = type;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}
	
	
	
	

}
